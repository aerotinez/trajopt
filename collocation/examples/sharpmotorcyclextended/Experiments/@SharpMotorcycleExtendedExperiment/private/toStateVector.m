function X = toStateVector(obj,T)
    arguments
        obj (1,1) SharpMotorcycleExtendedExperiment;
        T table;
    end
    state_names = cellfun(@string,T.Properties.RowNames);
    X0 = zeros(1,obj.Problem.NumNodes);

    f = @(state,name,unit,x0,xf,xmax,xmin)State( ...
        obj.Problem, ...
        state, ...
        Unit(name,unit), ...
        X0, ...
        x0, ...
        xf, ...
        xmax, ...
        xmin ...
        );

    x = arrayfun(f,state_names,T.Name,T.Units,T.Initial,T.Final,T.Max,T.Min);
    X = StateVector(x);
end